// role_history の生成（社長・会長は直近3期分を追加）
// ────────────────────────────────────────────────────────────
function makeRoleHistory(directors) {
  const entries = [];
  for (const d of directors) {
    // 全取締役のhistoryを現任として登録（start_year=就任推定年, end_year=null）
    const startYear = d.meeting_year - (d.tenure_years_before_meeting ?? 1);
    entries.push({
      history_id: `${d.director_id}_director_${d.meeting_year}`,
      director_id: d.director_id,
      company_code: d.company_code,
      name: d.name,
      role_type: d.is_outside_director ? "outside_director" : d.is_president ? "president" : d.is_chair ? "chair" : "director",
      role_title: d.current_title,
      start_year: startYear,
      end_year: null,
      has_representative_authority: d.has_representative_authority,
      source_url: d.source_url,
      confidence: "Medium",
      notes: d.notes,
    });
  }
  return entries;
}

async function main() {
  const dirPath = path.join(DATA_DIR, "directors.json");
  const rhPath = path.join(DATA_DIR, "director_role_history.json");

  const existing = JSON.parse(await fs.readFile(dirPath, "utf8"));
  const existingIds = new Set(existing.map((d) => d.director_id));

  const toAdd = NEW_DIRECTORS.filter((d) => !existingIds.has(d.director_id));
  const merged = [...existing, ...toAdd].sort((a, b) =>
    a.company_code.localeCompare(b.company_code) || (a.meeting_year - b.meeting_year) || a.director_id.localeCompare(b.director_id)
  );
  await fs.writeFile(dirPath, JSON.stringify(merged, null, 2), "utf8");
  console.log(`✅ directors.json: ${toAdd.length}件追加（合計 ${merged.length}件）`);

  // role_history 追加
  const rhExisting = JSON.parse(await fs.readFile(rhPath, "utf8"));
  const rhExistingIds = new Set(rhExisting.map((r) => r.role_history_id));
  const newRh = makeRoleHistory(toAdd).filter((r) => !rhExistingIds.has(r.role_history_id));
  const rhMerged = [...rhExisting, ...newRh].sort((a, b) =>
    a.company_code.localeCompare(b.company_code) || (a.fiscal_year - b.fiscal_year)
  );
  await fs.writeFile(rhPath, JSON.stringify(rhMerged, null, 2), "utf8");
  console.log(`✅ director_role_history.json: ${newRh.length}件追加（合計 ${rhMerged.length}件）`);

  // Summary
  const finalDirs = JSON.parse(await fs.readFile(dirPath, "utf8"));
  const companiesWithDirs = [...new Set(finalDirs.map((d) => d.company_code))];
  console.log(`\n📊 取締役データ対象企業: ${companiesWithDirs.length}社`);
  console.log(companiesWithDirs.sort().join(", "));
}

main().catch((err) => { console.error("❌", err); process.exit(1); });
